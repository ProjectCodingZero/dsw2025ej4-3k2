    private void buttonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAgregarActionPerformed
        String nombreAnimal = textFieldNombre.getText();
        Sector sector = null;
        ArrayList<Sector> sectores = Persistencia.getSectores();
        sector = sectores.get(comboBoxSector.getSelectedIndex());
        
        String paisNombre = textFieldNombrePais1.getText();
        String paisID = textFieldCodigo.getText();
        TipoAlimentacion alimentacion = null;
        int edad = 0;
        double peso = 0;
        try{
            String pesostring = textFieldEdad1.getText();
            String varString = textFieldPeso.getText();
            peso = Float.parseFloat(varString);
            edad = Integer.parseInt(pesostring);
        }catch (java.lang.Exception e){
            edad = 0;
            peso = 0;
        }
        Especie especie = null;
        Pais pais = new Pais(paisNombre, paisID);
        switch (comboBoxAlimentacion.getSelectedIndex()) {
            case 0:
                alimentacion = TipoAlimentacion.CARNIVORO;
                especie = new Especie(paisNombre, alimentacion, peso);
                try{
                    Carnivoro carnivoro = new Carnivoro(edad, peso, especie, sector, pais);
                    Controlador.agregarAnimal(carnivoro);
                }catch (java.lang.Exception e){
                }
                break;
            case 1:
                alimentacion = TipoAlimentacion.HERBIVORO;
                especie = new Especie(paisNombre, alimentacion, peso);

                double valorFijo = 0;
                try {
                    String string =textFieldValorFijo.getText();
                    valorFijo = Float.parseFloat(string);
                } catch (Exception e) {
                    valorFijo = 0;
                    e.printStackTrace();
                }
                try{
                    Herbivoro herbivoro = new Herbivoro(edad, peso, especie, sector, valorFijo, pais);
                    Controlador.agregarAnimal(herbivoro);
                    }catch (java.lang.Exception e){
                        e.printStackTrace();
                }
                break;
            case 2:
                alimentacion = TipoAlimentacion.OMNIVORO;
                break;
            default:
                alimentacion = null;
        }
        
        textFieldCodigo.setText("");
        textFieldEdad1.setText("");
        textFieldNombre.setText("");
        textFieldNombrePais1.setText("");
        textFieldPeso.setText("");
        textFieldValorFijo.setText("");
        textFieldValorFijo.setEnabled(false);
    }//GEN-LAST:event_buttonAgregarActionPerformed

