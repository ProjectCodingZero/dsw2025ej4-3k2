    private void getSector(){
        ArrayList<Sector> sectores = Persistencia.getSectores();
        Vector<String> sectorString = new Vector<>();
        for(Sector sector: sectores){
            sectorString.add(sector.toString());
        }
        comboBoxSector.setModel(new DefaultComboBoxModel(sectorString));
    }
    private void getAlimentacion(){
        Vector<TipoAlimentacion> alimentacion = new Vector<>();
        alimentacion.add(TipoAlimentacion.CARNIVORO);
        alimentacion.add(TipoAlimentacion.HERBIVORO);
        alimentacion.add(TipoAlimentacion.OMNIVORO);
        Vector<String> alimentacionString = new Vector<>();
        for(TipoAlimentacion tipoAlimentacion : alimentacion){
            alimentacionString.add(tipoAlimentacion.toString());
        }
        comboBoxAlimentacion.setModel(new DefaultComboBoxModel<>(alimentacionString));
    }
